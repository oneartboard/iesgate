			  <h3>What is PSU?			</h3>
			 <h2 class="heading">
				PSU (PUBLIC SECTOR UNDERTAKING)

			 </h2>
			<p class="read">
				Public sector undertaking company (PSU) is a Government owned corporation or a company in which central or state government have more than 51% share. Basically these government companies run like private companies and share profits to Government.
			</p>

			<p class="read">
				PSUâ€™s in India, for long, have had troubles conducting their recruitment processes with more than 100,000 students taking the exams for less than 1000 jobs (a selection rate of less than 1%). After sensing the exponential rise in the number of engineering graduates in India who wish to get a PSU job, the PSUs have decided that a GATE score shall be the primary criteria for initial shortlisting. This change was the primary cause for the rapid increase in GATE 2012.
			</p>
			<p class="read">
				Usually these companies release their recruitment notifications right after GATE notification, indicating that candidates have to take GATE for a job in their organizations. Thus, GATE score is used as a screening tool for recruiting engineers at entry level positions.

			</p>
